def seed_database(db_path: str = "data/phc_maharashtra.db"):
    os.makedirs(os.path.dirname(db_path), exist_ok=True)
    conn = sqlite3.connect(db_path)
    conn.execute("""
        CREATE TABLE IF NOT EXISTS phcs (
            id    INTEGER PRIMARY KEY AUTOINCREMENT,
            name  TEXT NOT NULL,
            phone TEXT NOT NULL,
            lat   REAL NOT NULL,
            lon   REAL NOT NULL
        )
    """)
    conn.execute("DELETE FROM phcs")  # idempotent reseed
    conn.executemany(
        "INSERT INTO phcs (name, phone, lat, lon) VALUES (?, ?, ?, ?)",
        SAMPLE_PHCS,
    )
    conn.commit()
    conn.close()
    print(f"Seeded {len(SAMPLE_PHCS)} PHCs → {db_path}")


if __name__ == "__main__":
    seed_database()
